int	ft_str_is_alpha(char *str)
{
	int		i;
	char	j;

	i = 0;
	while (str[i] != '\0')
	{
		j = str[i];
		if (!((j >= 'a' && j <= 'z') || (j >= 'A' && j <= 'Z')))
			return (0);
		i++;
	}
	return (1);
}
